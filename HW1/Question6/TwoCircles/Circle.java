package TwoCircles;

public class Circle{
	private double x;
	private double y;
	private double radius;
	
	public void setX(double x){
		this.x = x;
	}
	
	public void setY(double y){
		this.y = y;
	}
	
	public void setRadius(double radius){
		this.radius = radius;
	}
	
	public double getX(){
		return x;
	}
	
	public double getY(){
		return y;
	}
	
	public double getRadius(){
		return radius;
	}
}